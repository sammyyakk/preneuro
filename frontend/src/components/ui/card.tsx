import * as React from "react"

import { cn } from "@/lib/utils"

function Card({
  className,
  size = "default",
  hover = false,
  ...props
}: React.ComponentProps<"div"> & { size?: "default" | "sm"; hover?: boolean }) {
  return (
    <div
      data-slot="card"
      data-size={size}
      className={cn(
        "group/card flex flex-col gap-4 overflow-hidden rounded-2xl bg-card py-5 text-card-foreground shadow-sm border border-border/50 backdrop-blur-sm transition-all duration-300",
        hover && "hover:-translate-y-1 hover:shadow-xl hover:shadow-black/5 hover:border-border",
        "data-[size=sm]:gap-3 data-[size=sm]:py-4 data-[size=sm]:rounded-xl",
        className
      )}
      {...props}
    />
  )
}

